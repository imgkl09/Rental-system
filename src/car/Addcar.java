    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        String carid = txtcarid.getText();
        String cartype = comboCartype.getSelectedItem().toString();
        String brand = txtbrand.getText();
        String model = txtmodel.getText();
        String fuel = comboFuel.getSelectedItem().toString();
        String myear = txtmyear.getText();
        String cost = txtprice.getText();
        String avail=comboAvail.getSelectedItem().toString();
        String cregid=txtregid.getText();
        String gearType = "";
        if (jRadioButtonAuto.isSelected()) {
            gearType = "Automatic";
        }
        if (jRadioButtonManual.isSelected()) {
            gearType = "Manual";
        }
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/cardb", "root", "");
            pst = con.prepareStatement("insert into caraddrequest (car_id,car_type,brand,model,fuel_type,gear_type,model_year,price,available,car_regnum)values(?,?,?,?,?,?,?,?,?,?)");
            pst.setString(1,carid);
            pst.setString(2,cartype);
            pst.setString(3,brand);
            pst.setString(4,model);
            pst.setString(5,fuel);
            pst.setString(6,gearType);
            pst.setString(7,myear);
            pst.setString(8,cost);
            pst.setString(9,avail);
            pst.setString(10,cregid);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Request sent to Admin ....");
            Refresh();
        } 
        catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Addcar.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_submitButtonActionPerformed

