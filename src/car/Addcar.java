    public void autocarID()
    {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/cardb", "root","");
            Statement s=con.createStatement();
            ResultSet rs=s.executeQuery("select Max(car_id) from cars");
            rs.next();
            rs.getString("Max(car_id)");
            if(rs.getString("Max(car_id)")== null)
            {
                txtcarid.setText("A0001");
            }
            else
            {
                long id=Long.parseLong(rs.getString("Max(car_id)").substring(2,rs.getString("Max(car_id)").length()));
                id++;
                txtcarid.setText("A0" + String.format("%03d",id));
            }
            
        } 
        catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Addcar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
